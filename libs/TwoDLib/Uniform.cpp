#include <iostream>
#include "Uniform.hpp"

using namespace TwoDLib;

Uniform::Uniform(long seed):
_r(gsl_rng_alloc (gsl_rng_taus2)),
_seed(seed)
{
	gsl_rng_set (_r, seed);
}

Uniform::~Uniform()
{
	gsl_rng_free(_r);
}

double Uniform::GenerateNext() const
{
	return gsl_rng_uniform (_r);
}
